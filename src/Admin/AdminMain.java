    private void fetchLatestOrdersFromDatabase(java.util.Date selectedDate) {
        autoCancelExpiredOrders();
        String sql;
             if (selectedDate == null) {
                 sql = "SELECT o.orderNumber, "
                     + "GROUP_CONCAT(DISTINCT CONCAT(oi.quantity, 'x ', m.itemName) SEPARATOR ', ') AS allItems, "
                     + "IFNULL(GROUP_CONCAT(DISTINCT d.drinkName SEPARATOR ', '), 'None') AS allDrinks, "
                     + "IFNULL(GROUP_CONCAT(DISTINCT a.addonName SEPARATOR ', '), 'None') AS allAddons, "
                     + "(SELECT SUM(quantity) FROM order_items WHERE orderId = o.orderId) AS totalQuantity, "
                     + "o.total AS basePrice, o.discountAmount, o.finalTotal, o.createdAt, o.orderStatus "
                     + "FROM orders o JOIN order_items oi ON o.orderId = oi.orderId "
                     + "LEFT JOIN menu_Items m ON oi.menuItemId = m.itemID "
                     + "LEFT JOIN order_item_drinks oid ON oi.orderItemId = oid.orderItemId "
                     + "LEFT JOIN drinks d ON oid.drinkId = d.drinkID "
                     + "LEFT JOIN order_item_addons oia ON oi.orderItemId = oia.orderItemId "
                     + "LEFT JOIN add_ons a ON oia.addonId = a.addonId "
                     + "GROUP BY o.orderId, o.orderNumber, o.total, o.discountAmount, o.finalTotal, o.createdAt, o.orderStatus "
                     + "ORDER BY o.createdAt DESC";
             } else {
                 sql = "SELECT o.orderNumber, "
                     + "GROUP_CONCAT(DISTINCT CONCAT(oi.quantity, 'x ', m.itemName) SEPARATOR ', ') AS allItems, "
                     + "IFNULL(GROUP_CONCAT(DISTINCT d.drinkName SEPARATOR ', '), 'None') AS allDrinks, "
                     + "IFNULL(GROUP_CONCAT(DISTINCT a.addonName SEPARATOR ', '), 'None') AS allAddons, "
                     + "(SELECT SUM(quantity) FROM order_items WHERE orderId = o.orderId) AS totalQuantity, "
                     + "o.total AS basePrice, o.discountAmount, o.finalTotal, o.createdAt, o.orderStatus "
                     + "FROM orders o JOIN order_items oi ON o.orderId = oi.orderId "
                     + "LEFT JOIN menu_Items m ON oi.menuItemId = m.itemID "
                     + "LEFT JOIN order_item_drinks oid ON oi.orderItemId = oid.orderItemId "
                     + "LEFT JOIN drinks d ON oid.drinkId = d.drinkID "
                     + "LEFT JOIN order_item_addons oia ON oi.orderItemId = oia.orderItemId "
                     + "LEFT JOIN add_ons a ON oia.addonId = a.addonId "
                     + "WHERE DATE(o.createdAt) = ? "
                     + "GROUP BY o.orderId, o.orderNumber, o.total, o.discountAmount, o.finalTotal, o.createdAt, o.orderStatus "
                     + "ORDER BY o.createdAt DESC";
             }

             try{
                 PreparedStatement pstmt = conn.prepareStatement(sql);
                 if (selectedDate != null) {
                     pstmt.setDate(1, new java.sql.Date(selectedDate.getTime()));
                 }
                 rs = pstmt.executeQuery();
                 DefaultTableModel model = (DefaultTableModel) orderLogsTable.getModel();
                 model.setRowCount(0); 
                 while (rs.next()) {
                     java.util.Date orderDate = rs.getTimestamp("createdAt");
                     String displayDate = "";

                 if (orderDate != null) {

                     SimpleDateFormat sdf = new SimpleDateFormat("MMM dd, yyyy - hh:mm a");
                     displayDate = sdf.format(orderDate);
                 }

                     String rawStatus = rs.getString("orderStatus");
                     String displayStatus = rawStatus.substring(0, 1).toUpperCase() + rawStatus.substring(1);


                     Vector<Object> v1 = new Vector<>();
                         v1.add(rs.getInt("orderNumber"));
                         v1.add(rs.getString("allItems"));      
                         v1.add(rs.getString("allDrinks"));     
                         v1.add(rs.getString("allAddons"));
                         v1.add(rs.getInt("totalQuantity"));
                         v1.add(rs.getDouble("basePrice"));
                         v1.add(rs.getDouble("discountAmount")); 
                         v1.add(rs.getDouble("finalTotal"));
                         v1.add( displayDate);
                         v1.add(displayStatus);
                         model.addRow(v1);

                 }

             } catch (SQLException ex) {
                 System.err.println("Database error while fetching order logs!");
                 ex.printStackTrace();
             }
        }

        private Set<String> getDatesWithOrders() {
        Set<String> validDates = new HashSet<>();
        String sql = "SELECT DISTINCT DATE_FORMAT(createdAt, '%Y-%m-%d') AS orderDate FROM orders";
        try  
        {
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();

            while (rs.next()) {
                validDates.add(rs.getString("orderDate")); 
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        return validDates;
    }
    
    private void updateTableFilters() {
        if (sorter == null) return;
        List<RowFilter<Object, Object>> filters = new ArrayList<>();

        String selectedStatus = orderStatusComboBox.getSelectedItem().toString();
        if (!selectedStatus.equals("All")) {
            filters.add(RowFilter.regexFilter("^" + selectedStatus + "$", 9));
        }

        String searchText = orderNoSearchBar.getText().trim();
        if (!searchText.isEmpty()) {
            filters.add(RowFilter.regexFilter("(?i)" + searchText)); 
        }

        if (filters.isEmpty()) {
            sorter.setRowFilter(null); 
        } else {
            sorter.setRowFilter(RowFilter.andFilter(filters));
        }
    }
    
    public void autoCancelExpiredOrders() {
        
        String sql = "UPDATE orders SET orderStatus = 'cancelled' " +
                     "WHERE orderStatus = 'pending' " +
                     "AND createdAt < NOW() - INTERVAL 30 MINUTE";
                     
        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            int canceledCount = ps.executeUpdate();

            if (canceledCount > 0) {
                System.out.println("Auto-cleaned " + canceledCount + " expired pending orders.");
            }
        } catch (SQLException ex) {
            System.err.println("Error auto-canceling orders: " + ex.getMessage());
        }
    }
    
