    public void toggleSideNavigation() {
        // Check if the Create Order table has any items in it
        boolean isCartEmpty = tableCreateOrder.getRowCount() == 0;
        
        if (!isCartEmpty) {
            // LOCKDOWN: Cart has items, disable everything on the side panel
            createOrderButton.setEnabled(false);
            payOrderButton.setEnabled(false);
            orderLogsButton.setEnabled(false);
            editMenuButton.setEnabled(false);
            logoutButton.setEnabled(false);
        } else {
            // UNLOCK: Cart is empty, restore permissions safely!
            logoutButton.setEnabled(true);
            createOrderButton.setEnabled(true);
            payOrderButton.setEnabled(true);
            
            //Buttons enabled
            if ("admin".equalsIgnoreCase(userRole)) {
                orderLogsButton.setEnabled(true);
                editMenuButton.setEnabled(true);
            }
        }
    }
    
    public boolean verifyAdminCredentials() {
        javax.swing.JTextField usernameField = new javax.swing.JTextField();
        javax.swing.JPasswordField passwordField = new javax.swing.JPasswordField();
        Object[] message = {
            "Admin/Manager Override Required",
            "Username:", usernameField,
            "Password:", passwordField
        };

        int option = javax.swing.JOptionPane.showConfirmDialog(
            this, message, "Admin Authorization", 
            javax.swing.JOptionPane.OK_CANCEL_OPTION, 
            javax.swing.JOptionPane.WARNING_MESSAGE
        );
        
        if (option == javax.swing.JOptionPane.OK_OPTION) {
            String uname = usernameField.getText();
            String pass = new String(passwordField.getPassword());
            
            try {
                // Check if these credentials belong to an admin
                String sql = "SELECT * FROM users WHERE username = ? AND password = ? AND role = 'admin'";
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, uname);
                pst.setString(2, pass);
                java.sql.ResultSet rs = pst.executeQuery();
                
                if (rs.next()) {
                    return true; // Admin successfully verified!
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Invalid Admin Credentials!", "Access Denied", javax.swing.JOptionPane.ERROR_MESSAGE);
                    return false;
                }
            } catch (java.sql.SQLException e) {
                e.printStackTrace();
                return false;
            }
        }
        return false; // User clicked Cancel
    }
    
    private void generateOrderLogsReport() {
    try {
        // 1. Grab the visible table data (so search filtering works!)
        DefaultTableModel printModel = new DefaultTableModel();
        for (int i = 0; i < orderLogsTable.getColumnCount(); i++) {
            printModel.addColumn(orderLogsTable.getColumnName(i));
        }
        for (int i = 0; i < orderLogsTable.getRowCount(); i++) {
            Vector<Object> row = new Vector<>();
            for (int j = 0; j < orderLogsTable.getColumnCount(); j++) {
                row.add(orderLogsTable.getValueAt(i, j)); 
            }
            printModel.addRow(row);
        }

        // 2. Load the pre-compiled .jasper file using a portable relative path!
        File jasperFile = new File("C:\\Users\\jakob\\JaspersoftWorkspace\\MyReports\\OrderLogs.jasper");
        
        // Safety check to make sure you put the file in the right place
        if (!jasperFile.exists()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Could not find the .jasper file in the Reports folder!");
            return;
        }

        // Use JRLoader just like you suggested
        JasperReport jr = (JasperReport) JRLoader.loadObject(jasperFile);

        // 3. Fill the report using the filtered table data
        JRTableModelDataSource dataSource = new JRTableModelDataSource(printModel);
        HashMap<String, Object> parameters = new HashMap<>(); // Empty parameters map
        
        JasperPrint jp = JasperFillManager.fillReport(jr, parameters, dataSource);

        // 4. View the report
        JasperViewer.viewReport(jp, false);

    } catch (Exception ex) {
        ex.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Error loading report: " + ex.getMessage(), 
            "Report Error", 
            javax.swing.JOptionPane.ERROR_MESSAGE);
    }
}
    
    
