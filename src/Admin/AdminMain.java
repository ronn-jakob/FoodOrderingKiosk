class OrderDateEvaluator implements IDateEvaluator {
    private Set<String> validDates;
    private SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

    public OrderDateEvaluator(Set<String> validDates) {
        this.validDates = validDates;
    }

    // --- The Magic Method that disables empty dates ---
    @Override
    public boolean isInvalid(Date date) {
        if (date == null) return false;
        String formattedCalendarDate = sdf.format(date);
        return !validDates.contains(formattedCalendarDate);
    }

    // --- NEW: The Missing Tooltip Method ---
    @Override
    public String getInvalidTooltip() {
        // This is actually a cool feature! Now, if they hover over a grayed-out day, 
        // a little text box will pop up saying this:
        return "No orders recorded on this date"; 
    }

    // --- Required Dummy Methods for the Interface ---
    
    @Override
    public boolean isSpecial(Date date) { 
        return false; 
    }

    @Override
    public Color getSpecialForegroundColor() { 
        return null; 
    }

    // Note: The typo 'Backround' (missing the 'g') is actually hardcoded 
    // into the JCalendar library's source code!
    @Override
    public Color getSpecialBackroundColor() { 
        return null; 
    }

    @Override
    public String getSpecialTooltip() { 
        return null; 
    }

    @Override
    public Color getInvalidBackroundColor() {
        return null; // Null tells it to just use the default gray color
    }

    @Override
    public Color getInvalidForegroundColor() {
        return null; 
    }
}


